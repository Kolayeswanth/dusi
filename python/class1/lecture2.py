# escape sequences

print("hello \n world") #/n is used for new line
print("hello \t world") # /t is used for tab space
print("hello \\ world") # \\ is used for backslash
print("hello \' world") # /' is used for single quote in string
print("hello \" world") # /" is used for double quote in string
print("hello \b world") # /b is used for backspace
print("hello \r world") # /r is used for carriage return
print("hello \f world") # /f is used for form feed
print("hello \v world") # /v is used for vertical tab
print("hello \a world") # /a is used for alert (bell)