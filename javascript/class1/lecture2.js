// string handling in JavaScript

var name = "Kiran kumar pvn";
console.log(name);
console.log("Length of name: " + name.length);
console.log("Uppercase: " + name.toUpperCase());
console.log("Lowercase: " + name.toLowerCase());
console.log("First character: " + name.charAt(0));
console.log("Substring (0, 5): " + name.substring(0, 5));
console.log("Index of 'kumar': " + name.indexOf("kumar"));
console.log("Replace 'pvn' with 'Reddy': " + name.replace("pvn", "Reddy"));
console.log("Split by space: " + name.split(" "));